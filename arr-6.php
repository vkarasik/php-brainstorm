<?php
/**
 * Получить упорядоченный по возрастанию массив С(К) путем слияния двух порядоченных по возрастанию массивов А(N) и В(М), где К=М+N.
 */
function merge($a, $b)
{
    $c = [];
    for ($i = 0; $i < count($a); $i++) {
        $c[$i] = $a[$i] + $b[$i];
        if ($i - 1 >= 0 && $c[$i - 1] > $c[$i]) {
            $tmp = $c[$i];
            $c[$i] = $c[$i - 1];
            $c[$i - 1] = $tmp;
        }
    }
    print_r($c);
}

merge([1, 2, 3, 4, 5], [10, 20, 30, 40, 50]);
